#include "white_box_renderer.h"

WhiteBoxRenderer::WhiteBoxRenderer(Shader shader, Camera &camera)
        : camera(camera)
{
    this->shader = shader;
    this->init();
}

WhiteBoxRenderer::~WhiteBoxRenderer()
{}

